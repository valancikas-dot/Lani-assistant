"""
episodic_memory_service.py – Lani's autobiographical memory engine.

Automatically logs every event. Lani can recall:
  • What she did today / this week
  • Full conversation history with semantic search
  • Which tools she used and what happened
  • Her own reflections and observations

Public API
──────────
  log_conversation(session_id, user_msg, assistant_msg)
  log_tool_call(session_id, tool_name, args, result, success)
  log_observation(session_id, text, importance)
  log_reflection(session_id, text)
  recall(query, limit, days_back, event_type)  → list[EpisodicEvent]
  recall_today(session_id)                     → str (formatted summary)
  recall_week_summary()                        → str
"""

