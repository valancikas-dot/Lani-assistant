log = logging.getLogger(__name__)


def _utcnow() -> datetime:
    """Return a timezone-aware UTC timestamp."""
    return datetime.now(timezone.utc)

_MAX_KEEP_DAYS = 90   # auto-prune events older than this
_EMBED_EVENTS = {"conversation", "reflection", "observation"}  # embed these for semantic search


# ─── Writers ──────────────────────────────────────────────────────────────────

async def log_conversation(
    session_id: str,
    user_msg: str,
    assistant_msg: str,
    importance: float = 0.5,
) -> None:
    """Log a conversation turn. Called automatically by chat_tool."""
    embedding = await _embed(f"{user_msg} {assistant_msg}")
    async with AsyncSessionLocal() as db:
        db.add(EpisodicEvent(
            event_type="conversation",
            session_id=session_id,
            user_message=user_msg[:2000],
            assistant_response=assistant_msg[:4000],
            importance=importance,
            embedding=json.dumps(embedding) if embedding else None,
        ))
        await db.commit()


